package org.blackcoinj.pos;

public class StakeModifierValues {
	
	private long stakeModifier;
    private long stakeModifierCheckSum;
    private long stakeTime;
    
	public long getStakeModifier() {
		return stakeModifier;
	}
	public void setStakeModifier(long stakeModifier) {
		this.stakeModifier = stakeModifier;
	}
	public long getStakeModifierCheckSum() {
		return stakeModifierCheckSum;
	}
	public void setStakeModifierCheckSum(long stakeModifierCheckSum) {
		this.stakeModifierCheckSum = stakeModifierCheckSum;
	}
	public long getStakeTime() {
		return stakeTime;
	}
	public void setStakeTime(long stakeTime) {
		this.stakeTime = stakeTime;
	}

}
